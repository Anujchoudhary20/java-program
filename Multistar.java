package javaclass;

public class Multistar {
	public static void main(String[] args) {
		System.out.println("star partel in java");
		for(int i=0;i<9;i++) {
			for(int j=1;j<=9;j++) {
				System.out.print(i );
			}
			System.out.println();	
		}
		
		
		for(int i=9;i>0;i--) {
			for(int j=1;j<=9;j++) {
				System.out.print(i);
			}
			System.out.println();	
		}	
	}

}
